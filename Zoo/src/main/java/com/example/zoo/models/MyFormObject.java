package com.example.zoo.models;

public class MyFormObject {
    private String category;
    private String price;
    private String ot;
    private String doo;
    private String name;
    private String sortUp;
    private String sortDown;

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getOt() {
        return ot;
    }

    public void setOt(String ot) {
        this.ot = ot;
    }

    public String getDoo() {
        return doo;
    }

    public void setDoo(String doo) {
        this.doo = doo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSortUp() {
        return sortUp;
    }

    public void setSortUp(String sortUp) {
        this.sortUp = sortUp;
    }

    public String getSortDown() {
        return sortDown;
    }

    public void setSortDown(String sortDown) {
        this.sortDown = sortDown;
    }
}
